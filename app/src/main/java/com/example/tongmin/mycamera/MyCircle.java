public class MyCircle {
    private float pointX;
    private float pointY;
    private float r;


    public float getPointX() {
        return pointX;
    }

    public float getPointY() {
        return pointY;
    }

    public float getR() {
        return r;
    }

    public void setPointX(float pointX) {
        this.pointX = pointX;
    }

    public void setPointY(float pointY) {
        this.pointY = pointY;
    }

    public void setR(float r) {
        this.r = r;
    }
}
